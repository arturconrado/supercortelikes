  printf 'POSTGRES_PASSWORD=%s\n' "${POSTGRES_PASSWORD:-$(random_hex)}"
  printf 'POSTGRES_LOCAL_PORT=%s\n' "${POSTGRES_LOCAL_PORT:-55432}"
  printf '\n'
  printf 'REDIS_PASSWORD=%s\n' "${REDIS_PASSWORD:-$(random_hex)}"
