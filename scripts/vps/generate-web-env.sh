  printf 'MAX_UPLOAD_SIZE_BYTES=%s\n' "${MAX_UPLOAD_SIZE_BYTES:-5368709120}"
  printf 'UPLOAD_PART_SIZE_BYTES=%s\n' "${UPLOAD_PART_SIZE_BYTES:-67108864}"
  printf 'UPLOAD_QUEUE_SIZE=%s\n' "${UPLOAD_QUEUE_SIZE:-2}"
  printf 'UPLOAD_ALLOWED_MIME_TYPES=%s\n' "${UPLOAD_ALLOWED_MIME_TYPES:-video/mp4,video/quicktime,video/webm,video/x-matroska,video/x-msvideo}"
  printf '\n'
  printf 'JWT_SECRET=%s\n' "${JWT_SECRET:-$(random_base64)}"
  printf 'REFRESH_TOKEN_SECRET=%s\n' "${REFRESH_TOKEN_SECRET:-$(random_base64)}"
  printf 'MEDIA_WORKER_TOKEN=%s\n' "${MEDIA_WORKER_TOKEN:-$(random_base64)}"
  printf '\n'
  printf 'MEDIA_WORKER_TIMEOUT_MS=%s\n' "${MEDIA_WORKER_TIMEOUT_MS:-7200000}"
  printf 'MEDIA_MAX_CONCURRENT_JOBS=%s\n' "${MEDIA_MAX_CONCURRENT_JOBS:-2}"
  printf 'MEDIA_HEAVY_CONCURRENT_JOBS=%s\n' "${MEDIA_HEAVY_CONCURRENT_JOBS:-1}"
  printf 'MEDIA_LIGHT_CONCURRENT_JOBS=%s\n' "${MEDIA_LIGHT_CONCURRENT_JOBS:-4}"
  printf 'COMPOSITION_V1_ENABLED=%s\n' "${COMPOSITION_V1_ENABLED:-false}"
  printf 'COMPOSITION_V1_ROLLOUT_PERCENT=%s\n' "${COMPOSITION_V1_ROLLOUT_PERCENT:-100}"
  printf 'MEDIA_ACCELERATOR=%s\n' "${MEDIA_ACCELERATOR:-cpu}"
  printf 'FFMPEG_PRESET=%s\n' "${FFMPEG_PRESET:-veryfast}"
  printf 'FFMPEG_CRF=%s\n' "${FFMPEG_CRF:-19}"
