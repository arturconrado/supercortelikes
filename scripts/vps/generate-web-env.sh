  printf 'PUBLIC_API_URL=https://api.%s\n' "${app_domain}"
  printf 'CORS_ORIGIN=https://%s\n' "${app_domain}"
  printf 'CORS_ORIGINS=https://%s\n' "${app_domain}"
  printf 'S3_PUBLIC_ENDPOINT=https://storage.%s\n' "${app_domain}"
  printf 'S3_CORS_ALLOWED_ORIGINS_JSON=%s\n' "$(env_quote "[\"https://${app_domain}\"]")"
  printf 'GRAFANA_PUBLIC_URL=https://grafana.%s\n' "${app_domain}"
  printf '\n'
  printf 'VPS_PROVIDER=%s\n' "${VPS_PROVIDER:-digitalocean}"
  printf 'VPS_SIZE_PROFILE=%s\n' "${VPS_SIZE_PROFILE:-budget}"
  printf 'VPS_DATA_DIR=%s\n' "${VPS_DATA_DIR:-/srv/clipbr/data}"
  printf 'VPS_BACKUP_DIR=%s\n' "${VPS_BACKUP_DIR:-/srv/clipbr/backups}"
  printf '\n'
  printf 'NODE_ENV=production\n'
  printf 'APP_ENV=production\n'
  printf 'LOG_LEVEL=%s\n' "${LOG_LEVEL:-info}"
  printf 'BUILD_SHA=%s\n' "${BUILD_SHA:-${build_sha}}"
  printf 'RATE_LIMIT_MAX=%s\n' "${RATE_LIMIT_MAX:-300}"
  printf 'TERMS_VERSION=%s\n' "${TERMS_VERSION:-terms-2026-06}"
  printf 'PRIVACY_VERSION=%s\n' "${PRIVACY_VERSION:-privacy-2026-06}"
  printf 'JWT_ACCESS_TTL=%s\n' "${JWT_ACCESS_TTL:-15m}"
  printf 'JWT_REFRESH_DAYS=%s\n' "${JWT_REFRESH_DAYS:-30}"
