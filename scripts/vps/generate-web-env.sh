  printf '# Generated by scripts/vps/generate-web-env.sh. Do not commit this file.\n'
  printf 'APP_DOMAIN=%s\n' "${app_domain}"
  printf 'CADDY_EMAIL=%s\n' "${CADDY_EMAIL:-admin@${app_domain}}"
